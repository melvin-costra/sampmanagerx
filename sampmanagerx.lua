local function centeredText(text)
  local windowW = imgui.GetWindowSize().x
  local textW = imgui.CalcTextSize(text).x
  imgui.SetCursorPosX((windowW - textW) * 0.5)
  imgui.Text(text)
end

local function wrapTextLines(text, maxWidth)
  local lines = {}
  for paragraph in (text .. "\n"):gmatch("(.-)\n") do
    local line = ""
    for word in paragraph:gmatch("%S+") do
      local candidate = (line == "") and word or (line .. " " .. word)
      if line ~= "" and imgui.CalcTextSize(candidate).x > maxWidth then
        table.insert(lines, line)
        line = word
      else
        line = candidate
      end
    end
    table.insert(lines, line)
  end
  return lines
end

local function renderConfirmPopup()
  if confirmPopup.shouldOpen then
    imgui.OpenPopup(confirmPopup.id)
    confirmPopup.shouldOpen = false
  end

