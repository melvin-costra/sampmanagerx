  local clean = u8(text):gsub("~[nN]~", "\n"):gsub("~.-~", ""):gsub("^%s+", ""):gsub("%s+$", "")
  if clean == "" then return end
  fireEvent("game_text", clean)
end

function ev.onGivePlayerMoney(money)
  if MyMoney == money then return end
  local diff = money - MyMoney
  local sign = diff > 0 and "+" or ""
  local message = string.format("%s%s$ | Наличные: %s$", sign, formatMoney(diff), formatMoney(money))
  MyMoney = money
  fireEvent("money", message)
end

function ev.onShowDialog(dialogId, style, title, button1, button2, text)
  if not cfg.dialogs.options.enabled then return end
  if ws.GetConnectionStatus() ~= "OPEN" then return end

  local dialog = findDialogStyle(style)
  if not dialog then return end
  if not cfg.dialogs.options.sendAll and not dialog.is_enabled then return end

  local payload = table.concat({
    dialogId,
    u8(title or ""),
    u8(text or ""),
    u8(button1 or ""),
    u8(button2 or ""),
  }, DIALOG_SEPARATOR)

  sendTelegramMessage({
    type = OutcomingMessageType.DIALOG,
    tag = tostring(style),
    text = payload,
    disable_notification = not (cfg.dialogs.options.notifications and dialog.notification),
  })
end

