    tag = event.tag,
    text = text,
    disable_notification = not (cfg.events.options.notifications and event.notification),
  })
end

local function calculateZone(x, y, z)
  for i = 1, #STREETS do
    local v = STREETS[i]
    if (x >= v[2]) and (y >= v[3]) and (z >= v[4]) and (x <= v[5]) and (y <= v[6]) and (z <= v[7]) then
      return v[1]
    end
  end
  return 'Пригород'
end

local function updatePlayerCoordinates(prevCoordsText)
  if not (cfg.events.items.coordinates and cfg.events.items.coordinates.is_enabled) then
    return prevCoordsText
  end

  local x, y, z = getCharCoordinates(PLAYER_PED)
  local zone = calculateZone(x, y, z)
  local coordsText = string.format("x: %.0f, y: %.0f, z: %.0f | %s", x, y, z, zone)

  if coordsText ~= prevCoordsText then
    fireEvent("coordinates", coordsText)
    return coordsText
  end

  return prevCoordsText
end

local function updatePlayerHealth(prevHealth)
  if not (cfg.events.items.health and cfg.events.items.health.is_enabled) then
    return prevHealth
  end

  local health = getCharHealth(PLAYER_PED)

  if health ~= prevHealth then
    local text
    if prevHealth then
      local diff = health - prevHealth
      text = string.format("%.0f (%s%.0f)", health, diff > 0 and "+" or "", diff)
    else
      text = string.format("%.0f", health)
    end
    fireEvent("health", text)
    return health
  end

  return prevHealth
end

