function ev.onServerMessage(c, text)
  if ws.GetConnectionStatus() ~= "OPEN" then return end

  local decoded = u8(text)
  local message = handleChatMessage(c, decoded)

  if message then
    if type(message.text) == "string" then
      message.text = message.text:gsub("^[ \t]+", ""):gsub("\n[ \t]+", "\n")
    end
    sendTelegramMessage(message)
  elseif not collectingItem and (os.clock() - lastTelegramInputAt) < cfg.settings.forwardResponseTime then
    sendTelegramMessage({
      type = OutcomingMessageType.TEXT,
      tag = "CHAT",
      text = decoded:gsub("^[ \t]+", ""):gsub("\n[ \t]+", "\n"),
      disable_notification = true,
    })
  end
end

local function handleDamage(sign, preposition, playerId, damage, weapon, bodypart)
  local message = sign .. string.format("%d", math.ceil(damage)) .. " урона"
  if playerId >= 0 and playerId < 1000 then
    message = message .. " " .. preposition .. " " .. sampGetPlayerNickname(playerId) .. "[" .. playerId .. "]"
  end
  local reason = DAMAGE_WEAPONS[weapon] or tostring(weapon)
  local bodyPartName = BODY_PARTS[bodypart] or tostring(bodypart)
  message = message .. " | " .. reason .. " | " .. bodyPartName
  fireEvent("damage", message)
end

