local function calculateZone(x, y, z)
  for i = 1, #STREETS do
    local v = STREETS[i]
    if (x >= v[2]) and (y >= v[3]) and (z >= v[4]) and (x <= v[5]) and (y <= v[6]) and (z <= v[7]) then
      return v[1]
    end
  end
  return 'Пригород'
end

local function updatePlayerCoordinates(prevCoordsText)
  if not (cfg.events.items.coordinates and cfg.events.items.coordinates.is_enabled) then
    return prevCoordsText
  end

  local x, y, z = getCharCoordinates(PLAYER_PED)
  local zone = calculateZone(x, y, z)
  local coordsText = string.format("x: %.0f, y: %.0f, z: %.0f | %s", x, y, z, zone)

  if coordsText ~= prevCoordsText then
    fireEvent("coordinates", coordsText)
    return coordsText
  end

  return prevCoordsText
end

